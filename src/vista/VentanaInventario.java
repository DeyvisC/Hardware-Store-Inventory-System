    private void btnActualizarPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPrecioActionPerformed
        // 1. Verificar si hay una fila seleccionada en la tabla
    int filaSeleccionada = tblInventario.getSelectedRow();
    
    if (filaSeleccionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "¡Primero selecciona un producto de la tabla!");
        return;
    }

    // 2. Verificar que haya un precio nuevo escrito
    if (txtPrecio.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Escribe el nuevo precio en la casilla.");
        return;
    }

    try {
        // 3. Capturar el nuevo precio (convirtiendo texto a número)
        double nuevoPrecio = Double.parseDouble(txtPrecio.getText());
        
        // 4. ACTUALIZAR LA TABLA
        // setValueAt(VALOR, FILA, COLUMNA)
        // Según tu foto, el PRECIO es la Columna 2 (0=Nombre, 1=Stock, 2=Precio)
        tblInventario.setValueAt("S/ " + nuevoPrecio, filaSeleccionada, 2);
        
        // 5. Limpiar y avisar
        txtPrecio.setText("");
        javax.swing.JOptionPane.showMessageDialog(this, "¡Precio actualizado correctamente!");
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Ingrese un precio válido (solo números).");
    }

    }//GEN-LAST:event_btnActualizarPrecioActionPerformed

